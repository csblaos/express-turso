	function buildVariantLabel(value1: string, value2: string) {
		if (value1 && value2) return `${value1} / ${value2}`;
		return value1 || value2 || "ตัวเลือก";
	}

	function generateVariantMatrixRows() {
		if (!selectedProduct.value) return;
		const values1 = parseAxisValues(variantAxis1.valuesRaw);
		const values2 = variantAxis2Enabled.value ? parseAxisValues(variantAxis2.valuesRaw) : [];

		if (values1.length === 0) {
			productVariantMatrixError.value = "กรุณากรอกค่าแกนที่ 1 อย่างน้อย 1 ค่า";
			variantMatrixRows.value = [];
			return;
		}

		const combos: Array<{ value1: string; value2: string }> = [];
		if (values2.length > 0) {
			for (const v1 of values1) {
				for (const v2 of values2) {
					combos.push({ value1: v1, value2: v2 });
				}
			}
		} else {
			for (const v1 of values1) {
				combos.push({ value1: v1, value2: "" });
			}
		}

		if (combos.length > 200) {
			productVariantMatrixError.value = `จำนวนตัวเลือก ${combos.length} รายการเยอะเกินไป (จำกัด 200)`;
			variantMatrixRows.value = [];
			return;
		}

		const existingSkus = new Set<string>();
		const existingBarcodes = new Set<string>();
		for (const product of products.value) {
			const sku = String(product.sku || "").trim().toUpperCase();
			if (sku) existingSkus.add(sku);
			const barcode = String(product.barcode || "").trim();
			if (barcode && barcode !== "-") existingBarcodes.add(barcode);
		}

		const baseSku = String(selectedProduct.value.sku || "").trim().toUpperCase();
		if (baseSku) existingSkus.add(baseSku);

		const basePrice = normalizeMoneyTyping(String(selectedProduct.value.price ?? 0), { maxDecimals: 2 });
		const baseCost = normalizeMoneyTyping(String(selectedProduct.value.cost ?? 0), { maxDecimals: 2 });
		const takenSkus = new Set(existingSkus);

		const rows: VariantMatrixRow[] = combos.map((combo, index) => {
			const token1 = buildSkuToken(combo.value1);
			const token2 = buildSkuToken(combo.value2);
			const suffixParts = [ token1, token2 ].filter(Boolean);
			const suffix = suffixParts.join("-");
			const preferredSku = suffix ? `${baseSku}-${suffix}` : `${baseSku}-VAR`;
			const sku = makeSkuUniqueWithTaken(preferredSku, takenSkus);

			return {
				id: `${Date.now()}-${index}-${suffix || "var"}`,
				value1: combo.value1,
				value2: combo.value2,
				label: buildVariantLabel(combo.value1, combo.value2),
				sku,
				barcode: "",
				priceBase: basePrice,
				costBase: baseCost,
				active: true,
			};
		});

		productVariantMatrixError.value = null;
		variantMatrixRows.value = rows;
	}

