	function parseAxisValues(raw: string) {
		return String(raw || "")
			.split(/[,\n]/g)
			.map((value) => value.trim())
			.filter(Boolean)
			.filter((value, index, all) => all.indexOf(value) === index);
	}

	function buildSkuToken(raw: string) {
		const latin = toSkuSeed(raw);
		if (latin) return latin.split("-")[0]?.slice(0, 10) || "";

		const karaoke = toSkuSeed(transliterateThaiLaoKaraoke(raw));
		if (karaoke) return karaoke.split("-")[0]?.slice(0, 10) || "";

		return "";
	}

	function makeSkuUniqueWithTaken(preferred: string, taken: Set<string>) {
		const normalized = preferred.trim().toUpperCase();
		if (!normalized) return normalized;
		if (!taken.has(normalized)) {
			taken.add(normalized);
			return normalized;
		}

		for (let index = 2; index < 1000; index += 1) {
			const suffix = `-${index}`;
			const maxBaseLength = Math.max(1, 24 - suffix.length);
			const candidate = `${normalized.slice(0, maxBaseLength)}${suffix}`;
			if (!taken.has(candidate)) {
				taken.add(candidate);
				return candidate;
			}
		}

		const fallback = String(Date.now()).slice(-6);
		const candidate = `PRD-${fallback}`;
		taken.add(candidate);
		return candidate;
	}

	function makeBarcodeUniqueWithTaken(preferred: string, taken: Set<string>, prefix = "20") {
		const normalized = preferred.trim();
		if (!normalized) return normalized;
		if (!taken.has(normalized)) {
			taken.add(normalized);
			return normalized;
		}

		for (let attempt = 0; attempt < 128; attempt += 1) {
			const candidate = generateInternalEan13Barcode(prefix);
			if (!taken.has(candidate)) {
				taken.add(candidate);
				return candidate;
			}
		}

		return normalized;
	}

