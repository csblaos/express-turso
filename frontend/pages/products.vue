			`${source === "camera" ? "สแกนกล้อง" : "สแกน"} ${normalized} แล้วเติมลงช่องบาร์โค้ด`,
		);
		nextTick(() => {
			focusSearchInput();
		});
		return;
	}

	if (cameraScannerMode === "variant-barcode") {
		const rowId = variantBarcodeScanTargetRowId.value;
		const row = variantMatrixRows.value.find((item) => item.id === rowId);
		if (row) {
			row.barcode = normalized;
			triggerScanToast(`${source === "camera" ? "สแกนกล้อง" : "สแกน"} ${normalized} แล้วเติมลงช่องบาร์โค้ดตัวเลือก`);
		}
		nextTick(() => {
			focusSearchInput();
		});
		return;
	}

	searchQuery.value = normalized;
	const lower = normalized.toLowerCase();
	const matchedProduct = products.value.find((product) =>
		product.barcode.toLowerCase() === lower || product.sku.toLowerCase() === lower,
	) ?? filteredProducts.value[0];

	if (matchedProduct) {
		selectedProductId.value = matchedProduct.id;
