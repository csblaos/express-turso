function normalizeEan13ForPrint(raw: string) {
	const digits = String(raw || "").replace(/\D/g, "");
	if (digits.length === 13) return digits;
	if (digits.length === 12) {
		const checkDigit = computeEan13CheckDigit(digits);
		if (checkDigit === null) return null;
		return `${digits}${checkDigit}`;
	}
	return null;
}

function encodeEan13Bits(ean13: string) {
	if (!/^\d{13}$/.test(ean13)) return null;
	const parity = EAN13_PARITY[ean13[0]];
	if (!parity) return null;
	let bits = "101";
	for (let index = 1; index <= 6; index += 1) {
		const digit = ean13[index];
		const mode = parity[index - 1];
		const pattern = mode === "G" ? EAN13_G[digit] : EAN13_L[digit];
		if (!pattern) return null;
		bits += pattern;
	}
	bits += "01010";
	for (let index = 7; index <= 12; index += 1) {
		const digit = ean13[index];
		const pattern = EAN13_R[digit];
		if (!pattern) return null;
		bits += pattern;
	}
	bits += "101";
	return bits.length === 95 ? bits : null;
}

function ean13BitsToBars(bits: string) {
	const bars: Array<{ x: number; w: number }> = [];
	let runStart = -1;
	for (let index = 0; index < bits.length; index += 1) {
		const bit = bits[index];
		if (bit === "1" && runStart === -1) {
			runStart = index;
			continue;
		}
		if (bit === "0" && runStart !== -1) {
			bars.push({ x: runStart, w: index - runStart });
			runStart = -1;
		}
	}
	if (runStart !== -1) {
		bars.push({ x: runStart, w: bits.length - runStart });
	}
	return bars;
}

	function generateInternalEan13Barcode(prefix = "20") {
		// Prefix 20–29 is commonly used for store-internal EAN-13 (ลดชนกับบาร์โค้ดสินค้าทั่วไป)
		const safePrefix = String(prefix).replace(/\D/g, "").slice(0, 2).padStart(2, "2");
		const timePart = `${Date.now()}`.slice(-8).padStart(8, "0");
		const randomPart = Math.floor(Math.random() * 100).toString().padStart(2, "0");
		const base12 = `${safePrefix}${timePart}${randomPart}`;
		const checkDigit = computeEan13CheckDigit(base12);
		if (checkDigit === null) return `${Date.now()}`.slice(-13).padStart(13, "0");
		return `${base12}${checkDigit}`;
	}

