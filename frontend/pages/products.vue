const LAO_KARAOKE_IGNORE = new Set([ "່", "້", "໊", "໋", "໌", "ໆ", "ฯ" ]);

function toSkuSeed(raw: string) {
	return raw
		.normalize("NFKD")
		.replace(/[^\w\s-]/g, " ")
		.trim()
		.replace(/\s+/g, "-")
		.replace(/-+/g, "-")
		.replace(/[^A-Za-z0-9-]/g, "")
		.toUpperCase();
}

function transliterateThaiLaoKaraoke(raw: string) {
	let output = "";
	for (const char of raw) {
		if (char >= "\u0E00" && char <= "\u0E7F") {
			if (THAI_KARAOKE_IGNORE.has(char)) continue;
			output += THAI_KARAOKE_MAP[char] ?? "";
			continue;
		}
		if (char >= "\u0E80" && char <= "\u0EFF") {
			if (LAO_KARAOKE_IGNORE.has(char)) continue;
			output += LAO_KARAOKE_MAP[char] ?? "";
			continue;
		}
		output += char;
	}
	return output;
}

function buildSkuFromName(name: string) {
	const latinSeed = toSkuSeed(name);
	if (latinSeed) {
		return latinSeed.slice(0, 24);
	}

	const karaokeSeed = toSkuSeed(transliterateThaiLaoKaraoke(name));
	if (karaokeSeed) {
		return karaokeSeed.slice(0, 24);
	}

	const fallback = String(Date.now()).slice(-6);
	return `PRD-${fallback}`;
}

function makeSkuUnique(baseSku: string) {
	const normalized = baseSku.trim().toUpperCase();
	if (!normalized) return normalized;

	const taken = new Set<string>();
	for (const product of products.value) {
		const sku = String(product.sku || "").trim().toUpperCase();
		if (sku) taken.add(sku);
	}

	if (!taken.has(normalized)) return normalized;

	for (let index = 2; index < 1000; index += 1) {
		const suffix = `-${index}`;
		const maxBaseLength = Math.max(1, 24 - suffix.length);
		const candidate = `${normalized.slice(0, maxBaseLength)}${suffix}`;
		if (!taken.has(candidate)) return candidate;
	}

	const fallback = String(Date.now()).slice(-6);
	return `PRD-${fallback}`;
}

function makeBarcodeUnique(preferred: string) {
	const normalized = preferred.trim();
	if (!normalized) return normalized;

	const taken = new Set<string>();
	for (const product of products.value) {
		const barcode = String(product.barcode || "").trim();
		if (barcode) taken.add(barcode);
	}

	if (!taken.has(normalized)) return normalized;

	for (let attempt = 0; attempt < 128; attempt += 1) {
		const candidate = generateInternalEan13Barcode();
		if (!taken.has(candidate)) return candidate;
	}

	return normalized;
}

function computeEan13CheckDigit(base12: string) {
	const digits = base12.replace(/\D/g, "");
	if (digits.length !== 12) return null;
	let sum = 0;
	for (let index = 0; index < 12; index += 1) {
		const digit = Number(digits[index]);
		if (!Number.isFinite(digit)) return null;
		sum += (index % 2 === 0) ? digit : digit * 3;
	}
	return (10 - (sum % 10)) % 10;
}

