	async function saveVariantMatrix() {
		if (productVariantMatrixSaving.value) return;
		if (!selectedProduct.value) return;
		if (!canCreateProduct.value) return;

		productVariantMatrixError.value = null;
		const rows = variantMatrixRows.value;
		if (!rows.length) {
			productVariantMatrixError.value = "กรุณาสร้างตารางตัวเลือกก่อน";
			return;
		}

		const axis1Key = toSkuSeed(variantAxis1.label).toLowerCase() || "axis1";
		const axis2Key = toSkuSeed(variantAxis2.label).toLowerCase() || "axis2";

		const skuSet = new Set<string>();
		const barcodeSet = new Set<string>();
		for (const row of rows) {
			const sku = row.sku.trim().toUpperCase();
			if (!sku) {
				productVariantMatrixError.value = "กรุณากรอก SKU ให้ครบทุกแถว";
				return;
			}
			if (skuSet.has(sku)) {
				productVariantMatrixError.value = `SKU ซ้ำในตาราง: ${sku}`;
				return;
			}
			skuSet.add(sku);

			const barcode = row.barcode.trim();
			if (barcode) {
				if (barcodeSet.has(barcode)) {
					productVariantMatrixError.value = `บาร์โค้ดซ้ำในตาราง: ${barcode}`;
					return;
				}
				barcodeSet.add(barcode);
			}

			const price = parseLocaleNumber(row.priceBase);
			const cost = parseLocaleNumber(row.costBase);
			if (!Number.isFinite(price) || price < 0) {
				productVariantMatrixError.value = `ราคาขายไม่ถูกต้อง: ${row.label}`;
				return;
			}
			if (!Number.isFinite(cost) || cost < 0) {
				productVariantMatrixError.value = `ต้นทุนไม่ถูกต้อง: ${row.label}`;
				return;
			}
		}

		productVariantMatrixSaving.value = true;
		try {
			const response = await apiFetch<ApiEnvelope<ApiBulkCreateVariantsResult>>(`/products/${selectedProduct.value.id}/variants/bulk`, {
				method: "POST",
				body: {
					model_name: selectedProduct.value.name,
					include_base_sale: variantIncludeBaseProductSale.value,
					axes: [
						{ key: axis1Key, label: variantAxis1.label },
						...(variantAxis2Enabled.value ? [{ key: axis2Key, label: variantAxis2.label }] : []),
					],
					variants: rows.map((row) => ({
						sku: row.sku.trim().toUpperCase(),
						barcode: row.barcode.trim() ? row.barcode.trim() : null,
						price_base: parseLocaleNumber(row.priceBase),
						cost_base: parseLocaleNumber(row.costBase),
						active: row.active ? 1 : 0,
						variant_label: row.label,
						variant_options: {
							[axis1Key]: row.value1,
							...(variantAxis2Enabled.value && row.value2 ? { [axis2Key]: row.value2 } : {}),
						},
					})),
				},
			});

			void response;
			appToast.success({
				title: "สร้างตัวเลือกแล้ว",
				description: `เพิ่ม ${variantMatrixRows.value.length} SKU`,
				timeout: 2800,
			});

			productVariantMatrixOpen.value = false;
			variantMatrixRows.value = [];
			await loadProducts();
		} catch (error) {
			productVariantMatrixError.value = error instanceof Error ? error.message : "สร้างตัวเลือกไม่สำเร็จ";
			appToast.error({
				title: "สร้างตัวเลือกไม่ได้",
				description: productVariantMatrixError.value,
				timeout: 3600,
			});
		} finally {
			productVariantMatrixSaving.value = false;
		}
	}

